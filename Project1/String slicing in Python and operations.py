# String slicing in the Python
# String Operations

str1 = "EACODE"

print(str1[0:6])
str2 = len(str1)
print(str2)

# Slicing cases

print(str1[0:6])
print(str1[1:6])
print(str1[:6])
# These two statements have the same meanings
print(str1[0:-3])
print(str1[0:len(str1)-3])

print("The Length of the str1 variable is: ",len(str1))
